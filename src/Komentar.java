public class Komentar {
    public static void main(String[] args) {
        sum(10, 10);
    }

    /**
     * method untuk menjumlahkan value a dan value b
     * @param a
     * @param b
     */
    static void sum(int a, int b) {
        // menjumlahkan a dan b
        System.out.println(a + b);
    }
}
