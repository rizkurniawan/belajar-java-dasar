public class OperasiMatematika {
    public static void main(String[] args) {
        // operator matematika
        int a = 100;
        int b = 10;

        System.out.println(a + b);
        System.out.println(a - b);
        System.out.println(a * b);
        System.out.println(a / b);
        System.out.println(a % b);

        // augmented assignment
        System.out.println(a += 1);
        System.out.println(a -= 2);

        // unary operator
        int c = 100;

        System.out.println(++c);
        System.out.println(c--);
        System.out.println(-c);
        System.out.println(!true);
    }
}
