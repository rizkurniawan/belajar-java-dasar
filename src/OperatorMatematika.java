public class OperatorMatematika {
    public static void main(String[] args) {
        Integer a = 10;
        Integer b = 20;

        System.out.println(a+b);
        System.out.println(a-b);
        System.out.println(a*b);
        System.out.println(a/b);
        System.out.println(a%b);

        a++;
        System.out.println(a);
    }
}
