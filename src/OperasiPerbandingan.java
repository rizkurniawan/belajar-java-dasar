public class OperasiPerbandingan {
    public static void main(String[] args) {
        Integer a = 10;
        Integer b = 12;

        System.out.println(a > b);
        System.out.println(a < b);
        System.out.println(a <= b);
        System.out.println(a >= b);
        System.out.println(a != b);
    }
}
